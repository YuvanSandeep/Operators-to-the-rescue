num1 = 48384
num2 = 48294

# ==
result = num1 == num2
print (f"{num1} == {num2} is {result}")

# not ==
result = num1 != num2
print (f"{num1} is not == {num2} is {result}")

# >
result = num1 > num2
print (f"{num1} > {num2} is {result}")

# >=
result = num1 >= num2
print (f"{num1} >= {num2} is {result}")