num1 = 6389
num2 = 65

# addition
result = num1 + num2
print (f"{num1} + {num2} = {result}")

# subtraction
result = num1 - num2
print (f"{num1} - {num2} = {result}")

# multiplication
result = num1 * num2
print (f"{num1} * {num2} = {result}")

# division
result = num1 / num2
print (f"{num1} / {num2} = {result}")

# round division
result = num1 // num2
print (f"{num1} // {num2} = {result}")

# power
result = num1 ** num2
print (f"{num1} to the power of {num2} = {result}")

# modulus
result = num1 % num2
print (f"{num1} % {num2} = {result}")
